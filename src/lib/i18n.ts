export function saveLang(lang: Lang) {
  if (typeof window !== "undefined") {
    window.localStorage.setItem("lang", lang);
    document.documentElement.lang = lang;
    document.cookie = `lang=${lang}; path=/; max-age=${60 * 60 * 24 * 365}`;
  }
}
